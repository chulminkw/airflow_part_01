from airflow.sdk import DAG, task
from airflow.providers.common.sql.operators.sql import SQLExecuteQueryOperator
from airflow.providers.postgres.hooks.postgres import PostgresHook
from pendulum import datetime
import pandas as pd
import os

OUTPUT_DIR = "/tmp/postgres_load"
# мһ‘м—… мҲҳн–ү schema м„Өм •
SCHEMA = "stage"

"""
Task мҲҳн–үмӢң лі„лҸ„мқҳ search_pathлҘј м§Җм •н•ҳлҠ” л°©мӢқ
1. airflow connection мғқм„ұ мӢң extra н•„л“ңм—җ { "options": "-c search_path=мҠӨнӮӨл§ҲлӘ…"} м„Өм •
2. hookм—җ options м„Өм •
 2-1. SQLExecuteQueryOperatorм—җ hook_params мқёмһҗ м Ғмҡ©.  hook_params={"options": f"-c search_path=мҠӨнӮӨл§ҲлӘ…"}
 2-2. PostgresHook мғқм„ұ мӢң м¶”к°Җ мқёмһҗ м Ғмҡ©. options="-c search_path=мҠӨнӮӨл§ҲлӘ…"
"""

with DAG(
    dag_id="postgres_op_hook_02",
    start_date=datetime(2026, 1, 1, tz="Asia/Seoul"),
    schedule=None,
    catchup=False,
    tags=["operator_hook", "sql"],
) as dag:

    create_table_op = SQLExecuteQueryOperator(
        task_id="create_users_table",
        conn_id="postgres_delivery_conn",
        sql=f"""
        -- кё°мЎҙм—җ users н…Ңмқҙлё”мқҙ мһҲмңјл©ҙ drop table мҲҳн–ү
        DROP TABLE IF EXISTS users;

        /* users н…Ңмқҙлё”мқҙ м—Ҷмңјл©ҙ users н…Ңмқҙлё” мғқм„ұ */
        CREATE TABLE IF NOT EXISTS users (
        id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name VARCHAR(255),
        age INT,
        created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
        );
        """, 
        hook_params={"options": f"-c search_path={SCHEMA}"}
    )

    @task
    def insert_users():
        hook = PostgresHook(postgres_conn_id="postgres_delivery_conn",
                            options=f"-c search_path={SCHEMA}"
                            )

        sql=f"""
        INSERT INTO users (name, age) VALUES
        ('Alice', 30),
        ('Bob', 25),
        ('Charlie', 40);

        INSERT INTO users (name, age) VALUES
        ('Min', 50),
        ('Jane', 28),
        ('John', 20);

        COMMIT;
        """

        hook.run(sql=sql)


    @task
    def select_and_write_csv():
        hook = PostgresHook(postgres_conn_id="postgres_delivery_conn",
                            options=f"-c search_path={SCHEMA}"
                            )
        # м•„лһҳлҠ” sqlлӮҙм—җм„ң search_path лҘј л¶Җм—¬
        sql = """
        

        SELECT id, name, age, created_at, updated_at
        FROM users;
        """

        # MysqlHookмқҖ pandasмҷҖ мһҳ нҶөн•©лҗҳм–ҙ get_pandas_df(sql)мқ„ мҲҳн–үн•ҳл©ҙ sqlмқ„ мҲҳн–ү нӣ„ кІ°кіјлҘј pandasлЎң л°ҳнҷҳ
        # df = hook.get_pandas_df(sql)
        # лҳҗлҠ” м•„лһҳмҷҖ к°ҷмқҙ MysqlHookм—җм„ң db connection к°қмІҙлҘј к°Җм ё мҳЁ л’Ө,
        # pandasмқҳ read_sql()м—җ мҲҳн–ү sqlмҷҖ db connection к°қмІҙлҘј мһ…л Ҙ н•ҳм—¬, 
        # н•ҙлӢ№ sqlлҘј мҲҳн–үн•ҳкі  кІ°кіјлҘј DataFrameм—җ л°ҳнҷҳн•Ё. лҢҖмҡ©лҹү SelectмӢң л©”лӘЁлҰ¬ кіјлӢӨ мҶҢлӘЁ мң мқҳ
        conn = hook.get_conn()   # DB-API connection
        df = pd.read_sql(sql, conn)

        os.makedirs(OUTPUT_DIR, exist_ok=True)
        file_path = f"{OUTPUT_DIR}/users_postgres_02.csv"
        df.to_csv(file_path, index=False)

        # hook.get_conn()мқ„ мҲҳн–үн•ҳмҳҖмңјл©ҙ connectionмқ„ close мӢңнӮҙ
        conn.close()

        print(f"CSV written to {file_path}")
        return file_path

    create_table_op >> insert_users() >> select_and_write_csv()
